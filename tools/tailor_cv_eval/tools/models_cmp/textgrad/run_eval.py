import os
import sys
from pathlib import Path

from loguru import logger

sys.path.append(str(Path(__file__).resolve().parents[3]))
from config import LLM_PROMPT_OUTPUT_FILE, TMP_OUTPUT_DIR
from tools.ollama_helper import get_eval_model, get_models

# Route OpenAI client calls to Ollama local instance
os.environ["OPENAI_API_KEY"] = "ollama"
os.environ["OPENAI_BASE_URL"] = "http://localhost:11434/v1"

import textgrad as tg


def run_eval():
    eval_model = get_eval_model()
    models = get_models()
    subfolder = "job1"
    variant = "jd"
    gt_file = Path(f"inputs/{subfolder}/gt.md")
    prompt_file = Path(TMP_OUTPUT_DIR) / subfolder / LLM_PROMPT_OUTPUT_FILE

    if not gt_file.exists():
        logger.error(f"Ground truth file {gt_file} is missing.")
        return
    if not prompt_file.exists():
        logger.error(f"Prompt file {prompt_file} is missing. Please run prompt generation first.")
        return

    expected = gt_file.read_text(encoding="utf-8")
    prompt_content = prompt_file.read_text(encoding="utf-8")

    # Initialize Text-Grad engine with Ollama routed via OpenAI base url
    try:
        from textgrad.engine.openai import ChatOpenAI

        engine = ChatOpenAI(model_string=eval_model)
        tg.set_backward_engine(engine)
    except Exception as e:
        logger.error(f"Could not load Text-Grad engine: {e}")
        return

    logger.info(f"--- Text-Grad Evaluation (Variant: {variant}) ---")

    for model in models:
        logger.info(f"Generating CV with {model}...")
        try:
            from tools.ollama_helper import get_model_output

            model_output_file = (
                Path(TMP_OUTPUT_DIR) / subfolder / "model_output" / f"{model.replace(':', '_')}_{variant}_cv.md"
            )
            actual = get_model_output(model, prompt_content, model_output_file)
        except Exception as e:
            logger.error(f"Ollama generation failed for {model}: {e}")
            continue

        # Save output
        out_file = Path(f"{TMP_OUTPUT_DIR}/{subfolder}/textgrad/{model.replace(':', '_')}_{variant}_cv.md")
        out_file.parent.mkdir(parents=True, exist_ok=True)
        out_file.write_text(actual, encoding="utf-8")

        # Define variables for Text-Grad
        actual_var = tg.Variable(
            actual,
            requires_grad=True,
            role_description=f"the tailored CV generated by {model}",
        )

        evaluation_instruction = (
            f"Compare the tailored CV with the expected ground truth CV.\n\n"
            f"Expected Ground Truth:\n{expected}\n\n"
            f"Analyze the differences: identify missing sections, factual inconsistencies, or poor formatting."
        )

        loss_fn = tg.TextLoss(evaluation_instruction)

        # Evaluate (Forward pass) and get feedback (Backward pass)
        try:
            loss = loss_fn(actual_var)
            logger.info(f"Model: {model}")
            logger.info(f"  Loss Evaluation: {loss.value}")

            loss.backward()
            logger.info("  Textual Feedback for Improvement:")
            # Display collected textual gradients
            for grad in actual_var.gradients:
                logger.info(f"    - {grad}")
        except Exception as e:
            logger.error(f"Text-Grad evaluation failed for {model}: {e}")


if __name__ == "__main__":
    run_eval()
